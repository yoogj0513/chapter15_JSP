package com.yi.model;

public class Student {
	private int no;
	private String name;
	private int kor;
	private int math;
	private int eng;

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getKor() {
		return kor;
	}

	public void setKor(int kor) {
		this.kor = kor;
	}

	public int getMath() {
		return math;
	}

	public void setMath(int math) {
		this.math = math;
	}

	public int getEng() {
		return eng;
	}

	public void setEng(int eng) {
		this.eng = eng;
	}

	@Override
	public String toString() {
		return String.format("Student [no=%s, name=%s, kor=%s, math=%s, eng=%s]", no, name, kor, math, eng);
	}
}
